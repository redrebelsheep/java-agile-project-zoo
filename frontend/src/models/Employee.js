class Employee {
    #id;
    #name;
    #maintenanceCosts
    #job
    #enclosure
    #responsibilityAnimals
    #stall

    constructor(id, name, maintenanceCosts, job, enclosure, responsibilityAnimals, stall) {
        this.#id = id;
        this.#name = name;
        this.#maintenanceCosts = maintenanceCosts;
        this.#job = job;
        this.#enclosure = enclosure;
        this.#responsibilityAnimals = responsibilityAnimals;
        this.#stall = stall;
    }



}